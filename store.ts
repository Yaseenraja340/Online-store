interface Product {
    id: number;
    name: string;
    price: number;
    category: string;
}

type Cart = Product[];

interface Customer {
    id: number;
    name: string;
    email: string;
}

type Order = {
    customer: Customer;
    cart: Cart;
};

function calculateTotal(cart: Cart): number {
    let total = 0;
    for (const product of cart) {
        total += product.price;
    }
    return total;
}


const product1: Product = { id: 1, name: "Shirt", price: 50, category: "Clothing" };
const product2: Product = { id: 2, name: "Shoes", price: 60, category: "Footwear" };

const cart: Cart = [product1, product2];

const customer: Customer = { id: 1, name: "yaseen", email: "yaseenraja340@gmail.com" };

const order: Order = { customer, cart };

const totalPrice = calculateTotal(order.cart);
console.log(`Total price: $${totalPrice}`);